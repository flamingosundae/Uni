public class OpLogici {
    public static void main(String[] args)
    {

    }

    public static void opLogici(String[] args)
    {
        //Lista veloce degli operatori logici:
        if(true || false)
        {
            System.out.println("1");
        }
        if(true && false)
        {
            System.out.println("2");
        }
        if(true != false)
        {
            System.out.println(3);
        }
    }
}
